<h2>Summary</h2>

<p>The facelifted version of the second-generation Toyota Fortuner has made its way to the Indian market almost half a year after it was made its international debut in Thailand. Changes include a new stylish ‘Legender’ variant, a complete exterior design overhaul, new features, and a performance bump for the AT variants.</p>

<h3>Exterior</h3>

<p>For the standard variant, the fascia has received an evolutionary change with changes including new LED projector headlamps, a redesigned grille which loses its three chrome slat design and a completely new bumper with a more prominent front bash plate, larger faux side-intakes, and repositioned LED indicator strip. The Legender variant has a completely different fascia with a much slimmer grille, all-LED headlamps, larger lower air dam, and unique dual-tone paint schemes. The side design has not changed from the pre-facelift version, with the two variants differentiated by the larger 18-inch wheels. Changes at the rear are much less noticeable, the biggest one being the new LED elements in the taillights which now mimic those on the Camry sedan.</p>

<h3>Interior</h3>

<p>Inside, the Fortuner facelift has not received the radical changes as it has on the outside. There are new dual-tone upholsteries, the instrument cluster gets new graphics and the touchscreen infotainment system has a better display and better connectivity – including smartphone integration via Android Auto and Apple CarPlay. Some of the exciting new features added to the Fortuner include connected car technology and ventilated front seats. The Legender variant gets a 360-degree camera, wireless charging, radar-guided cruise control, lane departure warning and a steering wheel orientation warning.</p>

<h3>Engine</h3>

<p>The Toyota Fortuner facelift continues to be powered by the same 2.8-litre, four-cylinder, turbocharged diesel and 2.7-litre, naturally-aspirated, four-cylinder engine. The diesel engine, when paired with the manual gearbox, develops 201bhp / 420Nm but gets a torque boost to 500Nm when paired to the 6-speed torque converter automatic transmission. The diesel powertrain can be had with either a 4x2 or a 4x4 drivetrain. The petrol engine, which develops 164bhp / 245Nm can be had with both manual and automatic transmission but is only available with 4x2 drivetrain.</p>

<p>The Fortuner renews its rivalry with the likes of the updated Ford Endeavour, relatively new MG Gloster and the Mahindra Alturas G4.</p>
